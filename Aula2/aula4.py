numeroA = 2
numeroB = 3

#operadores aritméticos

#soma
resposta = numeroA + numeroB
print("resposta da soma: ", resposta)
print("resposta da soma: ", (numeroA + numeroB))

#subtração
respostaSub = numeroB - numeroA
print("resposta da subtração: ", respostaSub)
print("resposta da subtração: ", (numeroB - numeroA) )

#multiplicação
respostaMult = numeroA * numeroB
print("resposta da multiplicação: ", respostaMult)
print("resposta da multiplicação: ", (numeroA * numeroB) )